import face_recognition
import cv2
import os
import glob
import numpy as np
import pickle


path = os.path.realpath(__file__)
dir = os.path.dirname(path)
dir = dir.replace('detectfaces','encodeimages')
os.chdir(dir)

encodefile = open('encodings.pkl', "rb")
known_face_encodings = pickle.load(encodefile)
encodefile.close()

namesfile = open('names.pkl', "rb")
known_face_names = pickle.load(namesfile)
namesfile.close()

print("encodings = ", known_face_encodings)
print("names = ", known_face_names)


class SimpleFacerec:
    def __init__(self):
        self.frame_resizing = 0.75

    def detect_known_faces(self, frame):
        small_frame = cv2.resize(
            frame, (0, 0), fx=self.frame_resizing, fy=self.frame_resizing)
        # Find all the faces and face encodings in the current frame of video
        # Convert the image from BGR color (which OpenCV uses) to RGB color (which face_recognition uses)
        rgb_small_frame = cv2.cvtColor(small_frame, cv2.COLOR_BGR2RGB)
        face_locations = face_recognition.face_locations(rgb_small_frame)
        face_encodings = face_recognition.face_encodings(
            rgb_small_frame, face_locations)

        face_names = []
        for face_encoding in face_encodings:
            # See if the face is a match for the known face(s)
            matches = face_recognition.compare_faces(
                known_face_encodings, face_encoding)
            name = "Unknown"

            # # If a match was found in known_face_encodings, just use the first one.
            # if True in matches:
            #     first_match_index = matches.index(True)
            #     name = known_face_names[first_match_index]

            # Or instead, use the known face with the smallest distance to the new face
            face_distances = face_recognition.face_distance(
                known_face_encodings, face_encoding)
            best_match_index = np.argmin(face_distances)
            if matches[best_match_index]:
                name = known_face_names[best_match_index]
            face_names.append(name)

        # Convert to numpy array to adjust coordinates with frame resizing quickly
        face_locations = np.array(face_locations)
        face_locations = face_locations / self.frame_resizing
        return face_locations.astype(int), face_names
